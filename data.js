// 获取你的 Publish 站点根路径（一般为 "/" 或自定义域名路径）
const EV_SITE_BASE = window.location.origin + "/";

function evInit() {
  const appDiv = document.getElementById("ev-filter-app");
  if (!appDiv || appDiv.dataset.evLoaded === "true") return;
  if (typeof EV_DATA === "undefined") return;

  appDiv.dataset.evLoaded = "true";
  appDiv.innerHTML = "";

  // --- 工具函数 ---
  function parseNum(val) {
    if (!val) return null;
    return parseFloat(String(val).replace(/[^0-9.]/g, ""));
  }
  function unique(arr) {
    return [...new Set(arr.filter(Boolean))].sort();
  }
  function el(tag, attrs, children) {
    const elem = document.createElement(tag);
    for (const [k, v] of Object.entries(attrs || {})) {
      if (k === "text") elem.textContent = v;
      else if (k === "className") elem.className = v;
      else elem.setAttribute(k, v);
    }
    (children || []).forEach(c => {
      if (typeof c === "string") elem.appendChild(document.createTextNode(c));
      else if (c) elem.appendChild(c);
    });
    return elem;
  }

  // --- 收集选项 ---
  const communities = unique(EV_DATA.map(p => p.community));
  const projects = unique(EV_DATA.map(p => p.project));
  const stories = unique(EV_DATA.map(p => p.story));
  const wallMaterials = unique(EV_DATA.flatMap(p => p.wall_material || []));

  // --- 创建控件函数 ---
  function createSelect(parent, label, options) {
    const item = el("div", { className: "ev-filter-item" });
    item.appendChild(el("label", { text: label }));
    const select = el("select");
    select.appendChild(el("option", { value: "", text: "— 全部 —" }));
    options.forEach(opt =>
      select.appendChild(el("option", { value: opt, text: opt }))
    );
    item.appendChild(select);
    parent.appendChild(item);
    return select;
  }
  function createRangeInput(parent, label) {
    const item = el("div", { className: "ev-filter-item" });
    item.appendChild(el("label", { text: label }));
    const rd = el("div", { className: "ev-range-inputs" });
    const mn = el("input", { type: "number", placeholder: "最小值" });
    const mx = el("input", { type: "number", placeholder: "最大值" });
    rd.appendChild(mn);
    rd.appendChild(el("span", { text: " — " }));
    rd.appendChild(mx);
    item.appendChild(rd);
    parent.appendChild(item);
    return { min: mn, max: mx };
  }
  function createTriState(parent, label) {
    const item = el("div", { className: "ev-filter-item" });
    item.appendChild(el("label", { text: label }));
    const select = el("select");
    select.appendChild(el("option", { value: "", text: "— 全部 —" }));
    select.appendChild(el("option", { value: "true", text: "✅ 是" }));
    select.appendChild(el("option", { value: "false", text: "❌ 否" }));
    item.appendChild(select);
    parent.appendChild(item);
    return select;
  }

  // --- 构建筛选 UI ---
  const filterSection = el("div", { className: "ev-filter-section" });
  filterSection.appendChild(el("h3", { text: "🔍 筛选条件" }));
  const grid = el("div", { className: "ev-filter-grid" });

  const f = {};
  f.community = createSelect(grid, "📍 Community 社区", communities);
  f.project = createSelect(grid, "🏘️ Project 项目", projects);
  f.story = createSelect(grid, "🏢 Story 层数", stories);
  f.wall = createSelect(grid, "🧱 Wall Material 墙体材料", wallMaterials);
  f.revit = createTriState(grid, "📐 Revit Model");
  f.facade = createRangeInput(grid, "🏠 Facade Width (ft)");
  f.lot = createRangeInput(grid, "📏 Lot Width (ft)");
  f.depth = createRangeInput(grid, "📐 Lot Depth (ft)");

  filterSection.appendChild(grid);

  const btnRow = el("div", { className: "ev-btn-row" });
  const resetBtn = el("button", { className: "ev-btn", text: "🔄 重置筛选" });
  btnRow.appendChild(resetBtn);
  filterSection.appendChild(btnRow);

  appDiv.appendChild(filterSection);

  const resultsDiv = el("div");
  appDiv.appendChild(resultsDiv);

  // --- Lightbox ---
  let lightbox = document.querySelector(".ev-lightbox");
  if (!lightbox) {
    lightbox = el("div", { className: "ev-lightbox" });
    lightbox.appendChild(el("img", { src: "", alt: "" }));
    document.body.appendChild(lightbox);
    lightbox.addEventListener("click", () => lightbox.classList.remove("active"));
  }

  function openLightbox(src) {
    lightbox.querySelector("img").src = src;
    lightbox.classList.add("active");
  }

  // --- 图片路径转换 ---
  function resolveImgSrc(imgPath) {
    if (imgPath.startsWith("http")) return imgPath;
    // Obsidian Publish 的图片路径 = 站点根路径 + vault内路径
    // 空格等特殊字符需要编码
    return EV_SITE_BASE + encodeURI(imgPath);
  }

  // --- 笔记链接转换 ---
  function resolveNoteLink(filePath) {
    // 去掉 .md 后缀，Publish URL 不含 .md
    const clean = filePath.replace(/\.md$/, "");
    return EV_SITE_BASE + encodeURI(clean);
  }

  // --- 渲染 ---
  function renderResults() {
    resultsDiv.innerHTML = "";

    const cv = f.community.value;
    const pv = f.project.value;
    const sv = f.story.value;
    const wv = f.wall.value;
    const rv = f.revit.value;
    const fMin = f.facade.min.value ? parseFloat(f.facade.min.value) : null;
    const fMax = f.facade.max.value ? parseFloat(f.facade.max.value) : null;
    const lMin = f.lot.min.value ? parseFloat(f.lot.min.value) : null;
    const lMax = f.lot.max.value ? parseFloat(f.lot.max.value) : null;
    const dMin = f.depth.min.value ? parseFloat(f.depth.min.value) : null;
    const dMax = f.depth.max.value ? parseFloat(f.depth.max.value) : null;

    const filtered = EV_DATA.filter(p => {
      if (cv && p.community !== cv) return false;
      if (pv && p.project !== pv) return false;
      if (sv && p.story !== sv) return false;
      if (wv && !(p.wall_material || []).includes(wv)) return false;
      if (rv === "true" && !p.revit_model) return false;
      if (rv === "false" && p.revit_model) return false;
      const fw = parseNum(p.facade_width);
      if (fMin !== null && (fw === null || fw < fMin)) return false;
      if (fMax !== null && (fw === null || fw > fMax)) return false;
      const lw = parseNum(p.lot_width);
      if (lMin !== null && (lw === null || lw < lMin)) return false;
      if (lMax !== null && (lw === null || lw > lMax)) return false;
      const ld = parseNum(p.lot_depth);
      if (dMin !== null && (ld === null || ld < dMin)) return false;
      if (dMax !== null && (ld === null || ld > dMax)) return false;
      return true;
    });

    resultsDiv.appendChild(el("div", {
      className: "ev-count",
      text: `📋 共找到 ${filtered.length} 条结果`
    }));

    filtered.forEach(page => {
      const card = el("div", { className: "ev-result-card" });

      const h4 = el("h4");
      h4.appendChild(el("a", {
        text: page.elevation_id,
        href: resolveNoteLink(page.path)
      }));
      card.appendChild(h4);

      const props = el("div", { className: "ev-result-props" });
      if (page.facade_width) props.appendChild(el("span", { text: "🏠 Facade: " + page.facade_width }));
      if (page.lot_width) props.appendChild(el("span", { text: "📏 Lot W: " + page.lot_width }));
      if (page.lot_depth) props.appendChild(el("span", { text: "📐 Lot D: " + page.lot_depth }));
      if (page.story) props.appendChild(el("span", { text: "🏢 " + page.story + " Story" }));
      if (page.community) props.appendChild(el("span", { text: "📍 " + page.community }));
      if (page.wall_material && page.wall_material.length > 0)
        props.appendChild(el("span", { text: "🧱 " + page.wall_material.join(", ") }));
      if (page.project) props.appendChild(el("span", { text: "🏘️ " + page.project }));
      props.appendChild(el("span", {
        className: page.revit_model ? "ev-tag-revit" : "ev-tag-no-revit",
        text: page.revit_model ? "✅ Revit Model" : "❌ No Revit"
      }));
      card.appendChild(props);

      if (page.description && page.description.trim()) {
        card.appendChild(el("div", {
          className: "ev-result-desc",
          text: "💬 " + page.description
        }));
      }

      const imgGrid = el("div", { className: "ev-img-grid" });
      if (page.images && page.images.length > 0) {
        page.images.forEach(imgPath => {
          const src = resolveImgSrc(imgPath);
          const img = el("img", { src: src, alt: imgPath.split("/").pop(), loading: "lazy" });
          img.addEventListener("click", () => openLightbox(src));
          img.addEventListener("error", function () { this.style.display = "none"; });
          imgGrid.appendChild(img);
        });
      } else {
        imgGrid.appendChild(el("span", { className: "ev-no-img", text: "（无图片）" }));
      }
      card.appendChild(imgGrid);

      resultsDiv.appendChild(card);
    });
  }

  // --- 事件绑定 ---
  let debounceTimer;
  [f.community, f.project, f.story, f.wall, f.revit].forEach(s =>
    s.addEventListener("change", renderResults)
  );
  [f.facade.min, f.facade.max, f.lot.min, f.lot.max, f.depth.min, f.depth.max].forEach(input =>
    input.addEventListener("input", () => {
      clearTimeout(debounceTimer);
      debounceTimer = setTimeout(renderResults, 400);
    })
  );
  resetBtn.addEventListener("click", () => {
    f.community.value = "";
    f.project.value = "";
    f.story.value = "";
    f.wall.value = "";
    f.revit.value = "";
    f.facade.min.value = "";
    f.facade.max.value = "";
    f.lot.min.value = "";
    f.lot.max.value = "";
    f.depth.min.value = "";
    f.depth.max.value = "";
    renderResults();
  });

  renderResults();
}

// ==================== 页面加载与导航监听 ====================
// Obsidian Publish 是 SPA，页面切换不会完整刷新
// 需要用 MutationObserver 监听 DOM 变化

function evTryInit() {
  setTimeout(evInit, 300);
}

// 初始加载
evTryInit();

// 监听 Obsidian Publish 的页面切换
const evObserver = new MutationObserver(() => {
  const appDiv = document.getElementById("ev-filter-app");
  if (appDiv && appDiv.dataset.evLoaded !== "true") {
    evTryInit();
  }
});

evObserver.observe(document.body, { childList: true, subtree: true });
